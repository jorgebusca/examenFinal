    private void RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarActionPerformed
        Usuario user =new Usuario(this.jTextNombre.getText(), this.jTextApellido.getText(),
        Integer.parseInt(this.jTextDNI.getText()), 
        this.jTextDomicilio.getText(), 
        Integer.parseInt(this.jTextNumero.getText()), 
        Integer.parseInt(this.jTextPiso.getText()), 
        this.jTextDto.getText(), 
        this.jTextCiudad.getText(),
        Integer.parseInt(this.jTextCodArea.getText()), 
        Integer.parseInt(this.jTextCelular.getText()));
        
         user.setidUsuario(0);
         oper.altaUsuario(user);
    }//GEN-LAST:event_RegistrarActionPerformed

    private void BorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BorrarActionPerformed
       Usuario user =new Usuario(Integer.parseInt(this.IdUsuario.getText())); 
        
       user.setNombre("");
        user.setApellido("");
        user.setDni(0);
         user.setDomicilio("");
         user.setNro(0);
         user.setPiso(0);
         user.setDto("");
         user.setCiudad("");
         user.setCodArea(0);
         user.setCelular(0);
         
         oper.bajaUsuario(user);
    }//GEN-LAST:event_BorrarActionPerformed

